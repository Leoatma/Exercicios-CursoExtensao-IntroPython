## QUESTAO 01
## - Fazer programa que converte a velocidade de m/s para km/h. Dado: velocidade(m/s) = velocidade(km/h) * 3.6


veloc_m_s = float(input("Informe qual a velocidade do veículo, em m/s: "))

veloc_km_h = veloc_m_s * 3.6

print("The velocity {}m/s is {}km/h".format(veloc_m_s, veloc_km_h))

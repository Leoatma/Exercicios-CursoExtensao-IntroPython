## QUESTAO 01
## - Fazer programa que converte a velocidade de m/s para km/h. Dado: velocidade(m/s) = velocidade(km/h) * 3.6


velMetersSec = float(input("Informe qual a velocidade do veículo, em m/s: "))

velKmHour = velMetersSec * 3.6

print("The velocity {}m/s is {}km/h".format(velMetersSec, velKmHour))
